create table news(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR,
    full_title varchar,
    url VARCHAR,
    date timestamp
);

create table api_keys(
    id int generated by default as identity primary key,
    value varchar,
    owner_id int references api_owners(id)
);

create table api_owners(
    id int generated by default as identity primary key,
    email varchar,
    owner_name varchar
);